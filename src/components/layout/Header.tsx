import { useState, useEffect } from "react";
import { Link, useLocation } from "react-router-dom";
import { Menu, X, Phone, ArrowRight } from "lucide-react";
import { Button } from "@/components/ui/button";
import { BUSINESS_INFO } from "@/lib/constants";

const Header = () => {
  const [isOpen, setIsOpen] = useState(false);
  const [scrolled, setScrolled] = useState(false);
  const location = useLocation();
  const isHomePage = location.pathname === "/";

  useEffect(() => {
    const handleScroll = () => {
      setScrolled(window.scrollY > 20);
    };
    window.addEventListener("scroll", handleScroll);
    return () => window.removeEventListener("scroll", handleScroll);
  }, []);

  const navLinks = [
    { name: "Services", href: "/services" },
    { name: "Service Areas", href: "/locations" },
  ];

  const isActive = (href: string) => {
    if (href === "/") return location.pathname === "/";
    return location.pathname.startsWith(href);
  };





  return (
    <header
      className={`fixed top-0 left-0 w-full z-40 transition-all duration-300
        ${isHomePage && !scrolled ? 'bg-transparent shadow-none' : 'bg-card/95 shadow-lg border-b border-border'}
        backdrop-blur-xl
      `}
    >
      <div className="container mx-auto px-4 md:px-8 flex items-center justify-between h-16 md:h-20">
        {/* Brand */}
        <Link to="/" className="flex items-center gap-2 font-display font-bold text-xl md:text-2xl text-foreground">
          <span className="text-foreground">Fix</span>
          <span className="text-primary">Now</span>
          <span className="text-foreground hidden sm:inline"> Mechanics</span>
        </Link>

        {/* Desktop Nav */}
        <nav className="hidden md:flex items-center gap-6">
          {navLinks.map((link) => (
            <Link
              key={link.href}
              to={link.href}
              className={`text-base font-medium transition-colors px-2 py-1 rounded-md
                ${isActive(link.href) ? 'text-primary' : 'text-secondary hover:text-foreground'}`}
            >
              {link.name}
            </Link>
          ))}
          <Button asChild size="sm" className="ml-2 rounded-full font-semibold shadow-none">
            <Link to="/quote">
              Get Quote
              <ArrowRight className="w-4 h-4 ml-1" />
            </Link>
          </Button>
        </nav>

        {/* Mobile Hamburger */}
        <button
          className="md:hidden p-2 rounded-md text-foreground hover:bg-border/20 focus:outline-none focus:ring-2 focus:ring-primary"
          onClick={() => setIsOpen(true)}
          aria-label="Open menu"
        >
          <Menu className="w-7 h-7" />
        </button>
      </div>

      {/* Mobile Drawer */}
      {isOpen && (
        <div className="fixed inset-0 z-50 bg-background/90 backdrop-blur-xl flex flex-col">
          <div className="flex items-center justify-between px-4 pt-4 pb-2">
            <Link to="/" className="flex items-center gap-2 font-display font-bold text-xl text-foreground" onClick={() => setIsOpen(false)}>
              <span className="text-foreground">Fix</span>
              <span className="text-primary">Now</span>
            </Link>
            <button
              className="p-2 rounded-md text-foreground hover:bg-border/20 focus:outline-none focus:ring-2 focus:ring-primary"
              onClick={() => setIsOpen(false)}
              aria-label="Close menu"
            >
              <X className="w-7 h-7" />
            </button>
          </div>
          <nav className="flex flex-col gap-2 px-6 mt-6">
            {navLinks.map((link) => (
              <Link
                key={link.href}
                to={link.href}
                className={`text-lg font-medium py-3 px-2 rounded-md transition-colors
                  ${isActive(link.href) ? 'text-primary' : 'text-secondary hover:text-foreground'}`}
                onClick={() => setIsOpen(false)}
              >
                {link.name}
              </Link>
            ))}
            <Button asChild size="lg" className="mt-4 w-full rounded-full font-semibold shadow-none">
              <Link to="/quote" onClick={() => setIsOpen(false)}>
                Get Quote
                <ArrowRight className="w-4 h-4 ml-1" />
              </Link>
            </Button>
          </nav>
          {/* Bottom fixed call/whatsapp buttons */}
          <div className="mt-auto w-full px-6 pb-8 flex flex-col gap-3">
            <a
              href={`tel:${BUSINESS_INFO.phone}`}
              className="w-full flex items-center justify-center gap-2 py-3 rounded-full bg-primary text-primary-foreground font-semibold text-lg shadow-none hover:bg-primary/90 transition-all"
            >
              <Phone className="w-5 h-5" />
              Call Us
            </a>
            <a
              href={BUSINESS_INFO.whatsapp}
              target="_blank"
              rel="noopener noreferrer"
              className="w-full flex items-center justify-center gap-2 py-3 rounded-full bg-green-600 text-white font-semibold text-lg shadow-none hover:bg-green-700 transition-all"
            >
              <svg viewBox="0 0 24 24" fill="currentColor" className="w-5 h-5"><path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z"/></svg>
              WhatsApp
            </a>
          </div>
        </div>
      )}
    </header>
  );
};

export default Header;